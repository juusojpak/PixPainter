package fi.tamk.jpak.pixpainter.tools;

/**
 * Enum for shape fill types.
 *
 * OUTLINE - Draw only the outlines of the shape.
 * SOLID   - Outlines and fill with primary color.
 * FILL    - Outlines with primary color and fill with secondary color.
 *
 * @author Juuso Pakarinen
 * @version 09.05.2017
 */
public enum ShapeFillType {
    OUTLINE, SOLID, FILL
}
